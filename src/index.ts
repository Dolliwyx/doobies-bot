import './lib/setup';
import { DoobieClient } from './lib/structures/DoobieClient';

const client = new DoobieClient();

const main = async () => {
	try {
		client.logger.info('Logging in');
		await client.login();
		client.logger.info('logged in');
	} catch (error) {
		client.logger.fatal(error);
		client.destroy();
		process.exit(1);
	}
};

main();
